# Indentation In python
# 1)In python indentation is used to define block of code.
# 2)It tells the Python interpreter that a group of statements belongs to a specific block
# 3)All statements with the same level of indentation are considered part of the same block
# 4) Indentation is achieved using whitespace (spaces or tabs) at the beginning of each line.

